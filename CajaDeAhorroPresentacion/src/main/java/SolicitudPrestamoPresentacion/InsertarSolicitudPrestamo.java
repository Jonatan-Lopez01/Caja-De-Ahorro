    public void cargarListaSocios() {
        SocioDaoImpl enlace = new SocioDaoImpl();
        java.util.List<Socio> listaDeSocios = enlace.obtenerTodosLosSocios();

        DefaultTableModel modeloTabla = new DefaultTableModel();
        modeloTabla.addColumn("ID Socio");
        modeloTabla.addColumn("Nombre");
        modeloTabla.addColumn("Apellidos");
        modeloTabla.addColumn("Dirección");
        modeloTabla.addColumn("Teléfono");
        modeloTabla.addColumn("Correo");

        for (Socio socio : listaDeSocios) {
            modeloTabla.addRow(new Object[]{socio.getId_socio(), socio.getNombre(), socio.getApellidos(), socio.getDireccion(), socio.getTelefono(), socio.getCorreo()});
        }
        tablaSocios.setModel(modeloTabla);
    }

