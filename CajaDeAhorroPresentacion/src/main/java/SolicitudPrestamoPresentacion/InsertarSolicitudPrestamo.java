    private void btnBuscar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscar1ActionPerformed
        try {
            SocioDaoImpl enlace = new SocioDaoImpl();
            int idSocio = Integer.parseInt(txtBuscar.getText());
            Socio socioEncontrado = enlace.obtenerSocioPorId(idSocio);

            if (socioEncontrado != null) {
                DefaultTableModel modeloTabla = new DefaultTableModel();
                modeloTabla.addColumn("ID Socio");
                modeloTabla.addColumn("Nombre");
                modeloTabla.addColumn("Apellidos");
                modeloTabla.addColumn("Dirección");
                modeloTabla.addColumn("Teléfono");
                modeloTabla.addColumn("Correo");

                modeloTabla.addRow(new Object[]{
                    socioEncontrado.getId_socio(),
                    socioEncontrado.getNombre(),
                    socioEncontrado.getApellidos(),
                    socioEncontrado.getDireccion(),
                    socioEncontrado.getTelefono(),
                    socioEncontrado.getCorreo()
                });
                tablaSocios.setModel(modeloTabla);
            } else {
                // Si no se encuentra el socio, mostrar un mensaje de error o manejarlo según sea necesario
                System.out.println("Socio no encontrado");
            }
        } catch (NumberFormatException e) {
            // Manejar la excepción si el ID no es un número válido
            System.out.println("ID inválido");
        }

    }//GEN-LAST:event_btnBuscar1ActionPerformed

