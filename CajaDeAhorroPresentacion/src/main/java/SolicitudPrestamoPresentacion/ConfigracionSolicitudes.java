    private void CancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CancelActionPerformed
        // Obtener la ID de la solicitud a eliminar
        int idSolicitud = Integer.parseInt(idSoli.getText());

        // Verificar el estado de la solicitud
        SolicitudPrestamoDaoImpl enlace = new SolicitudPrestamoDaoImpl();
        SolicitudPrestamo solicitud = enlace.obtenerSolicitudPrestamoPorId(idSolicitud);

        if (solicitud != null) {
            String estado = solicitud.getEstado();
            if (!estado.equals("Aprobado") && !estado.equals("Aceptado")) {
                // Eliminar la solicitud
                enlace.eliminarSolicitudPrestamo(idSolicitud);
                // Mostrar mensaje de √©xito en la interfaz
                JOptionPane.showMessageDialog(this, "Solicitud cancelada correctamente.");
            } else {
                // Mostrar mensaje de error en la interfaz
                JOptionPane.showMessageDialog(this, "No se puede cancelar una solicitud aprobada o aceptada.");
            }
        } else {
            // Mostrar mensaje de error en la interfaz
            JOptionPane.showMessageDialog(this, "No se encontr√≥ la solicitud con el ID especificado.");
        }
    }//GEN-LAST:event_CancelActionPerformed

