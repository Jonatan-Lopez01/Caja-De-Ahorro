    public void cargarListaSolicitudes() {
        SolicitudPrestamoDaoImpl enlace = new SolicitudPrestamoDaoImpl();
        java.util.List<SolicitudPrestamo> listaDeSolicitudes = enlace.obtenerTodasLasSolicitudesPrestamo();

        DefaultTableModel modeloTabla = new DefaultTableModel();
        modeloTabla.addColumn("ID solicitud Prestamo");
        modeloTabla.addColumn("ID Socio");
        modeloTabla.addColumn("Monto Prestado");
        modeloTabla.addColumn("Numero de cuenta");
        modeloTabla.addColumn("Tipo de Prestamo");
        modeloTabla.addColumn("Fecha de Solicitud");
        modeloTabla.addColumn("Plazo");
        modeloTabla.addColumn("Numero de montos");
        modeloTabla.addColumn("Descripcion");
        modeloTabla.addColumn("Status");

        for (SolicitudPrestamo solicitud : listaDeSolicitudes) {
            modeloTabla.addRow(new Object[]{solicitud.getIdSolicitudPrestamo(), solicitud.getIdSocio(), solicitud.getMontoPrestado(), solicitud.getnumCuenta(), solicitud.getTipoPrestamo(), solicitud.getFechaSolicitud(), solicitud.getPlazo(), solicitud.getNumMontos(), solicitud.getDescripcion(), solicitud.getEstado()});
        }
        tablaUsuarios.setModel(modeloTabla);
    }

