    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if (txtIdSocio.getText().equalsIgnoreCase("") || txtIdSocioDestino.getText().equalsIgnoreCase("") || txtNumeroCuenta.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Debe rellenar todos los campos necesarios", "Error al buscar", JOptionPane.ERROR_MESSAGE);
        } else {
            int confirmacion = JOptionPane.showConfirmDialog(this, "¿Está seguro de querer actualizar este usuario?", "Actualizar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (confirmacion == JOptionPane.YES_OPTION) {
                SocioCuentaConexion enlace = new SocioCuentaConexion();
               

                enlace.actualizarSocioCuentaPorNumeroCuenta(Integer.parseInt(txtNumeroCuenta.getText()), Integer.parseInt(txtIdSocio.getText()), Integer.parseInt(txtIdSocioDestino.getText()));
                JOptionPane.showMessageDialog(this, "Cuenta traspasada correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            } else {
                // Usuario ha seleccionado "No" (Cancelar)
                System.out.println("error no se pudo hacer la transaccion");
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

