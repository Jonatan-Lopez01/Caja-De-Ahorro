    private void btnBuscarCuentasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarCuentasActionPerformed
        if (txtIdSocio.getText().equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "Seleccione un elemento de la tabla Socios", "Error al encontrar cuentas", JOptionPane.ERROR_MESSAGE);
        } else {
            SocioDaoImpl enlace = new SocioDaoImpl();
            java.util.List<Cuenta> listaDeCuentas = enlace.obtenerCuentasPorIdSocio(Integer.parseInt(txtIdSocio.getText()));

            DefaultTableModel modeloTabla = new DefaultTableModel();
            modeloTabla.addColumn("Número de Cuenta");
            modeloTabla.addColumn("Taza de Interés");
            modeloTabla.addColumn("Estatus de la Cuenta");
            modeloTabla.addColumn("Saldo");
            for (Cuenta cuenta : listaDeCuentas) {
                modeloTabla.addRow(new Object[]{cuenta.getNumeroCuenta(), cuenta.getTasaInteres(), cuenta.getEstatusCuenta(), cuenta.getSaldo()});
            }
            tablaCuentas.setModel(modeloTabla);
        }
    }//GEN-LAST:event_btnBuscarCuentasActionPerformed

    private void btnBuscar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscar1ActionPerformed
        // TODO add your handling code here:
        SocioDaoImpl enlace = new SocioDaoImpl();
        DefaultTableModel modeloTabla = new DefaultTableModel();
        modeloTabla.addColumn("ID Socio");
        modeloTabla.addColumn("Nombre");
        modeloTabla.addColumn("Apellidos");
        modeloTabla.addColumn("Dirección");
        modeloTabla.addColumn("Teléfono");
        modeloTabla.addColumn("Correo");

        if (txtBuscar.getText().isEmpty() || !txtBuscar.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "El campo de búsqueda no puede estar vacío y debe ser un numero", "Error al buscar", JOptionPane.ERROR_MESSAGE);
        } else {
            Socio socioEncontrado = enlace.obtenerSocioPorId(Integer.parseInt(txtBuscar.getText()));
            if (socioEncontrado != null) {
                modeloTabla.addRow(new Object[]{socioEncontrado.getId_socio(), socioEncontrado.getNombre(), socioEncontrado.getApellidos(), socioEncontrado.getDireccion(), socioEncontrado.getTelefono(), socioEncontrado.getCorreo()});
                tablaSocios.setModel(modeloTabla);
            } else {
                System.out.println("Hay que limpiar la tabla");
                modeloTabla.addRow(new Object[]{"", "", ""});
                tablaSocios.setModel(modeloTabla);
            }
        }
    }//GEN-LAST:event_btnBuscar1ActionPerformed

